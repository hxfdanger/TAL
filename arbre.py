class tree:

    def __init__(self):
        """
        constructor: tree
        input: sentence list[string]
        """
        self.vertices = []

    def push(vertex):
        """
        push vertex
        """
        self.vertices.append(vertex)

    def pop(i):
        """
        show and delete the i vertex in vertices
        """
        self.vertices.pop(i)


