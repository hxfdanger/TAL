class tree:

    def __init__(self):
        """
        constructor: tree
        input: sentence list[string]
        """
        self.vertices = []

    def push(vertex):
        """
        push vertex to the end of the tree
        """
        return self.vertices.append(vertex)

    def pop():
        """
        return the first vertex in the tree
        """
        return self.vertices.pop(0)


